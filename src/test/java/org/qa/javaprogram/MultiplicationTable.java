package org.qa.javaprogram;

public class MultiplicationTable {

	public static void main(String[] args) {
		//Multiplication Table:
		//2 x 1 = 2
		//2 x 2 = 4
		//2 x 3 = 6
		//2 x 4 = 8 
		
		int num=2;
		
		for(int i=1;i<=10;i++){
			System.out.println(num + " x " + i + " = " + num*i);
		}
		

	}

}
